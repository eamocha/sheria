    <div class="card mb-4">
        <div class="card-body">
            <h5 class="font-weight-bold">Headnote</h5>
            <p>This opinion addresses the obligations of employers when handling employee personal data under the Data Protection Act, 2019.</p>

            <h5 class="font-weight-bold">Summary</h5>
            <p>Employers must obtain consent before processing sensitive employee information and must implement safeguards for data security.
                Violations may lead to administrative penalties and liability under civil law.</p>
